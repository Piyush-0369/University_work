//sizeof datatypes//
#include <stdio.h>
int main(){
    int p=1;
    char q='c';
    float r=1.1;
    double s=2.2;
    printf("The sizeof int is: %d\n",sizeof(p));
    printf("The sizeof char is: %d\n",sizeof(q));
    printf("The sizeof float is: %d\n",sizeof(r));
    printf("The sizeof double is: %d\n",sizeof(s));
    return 0;
}